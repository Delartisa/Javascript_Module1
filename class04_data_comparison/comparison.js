// COMPARACAO DE DADOS | retorna resultado booleano (true or false)

/*
- operadores:
1. igual == / ===
2. diferente != / !==
3. maior que >
4. menor que <
5. maior ou igual a >=
6. menor ou igual a <=

* OBS: nos operadores 1 e 2, os com apenas um = comparam apenas o valor, e nao o tipo, enquanto os com === ou == comparam os dois.

ex: 
10 == "10" -> true (um e number e outro e uma string com o numero 10, mas como os dois sao 10, retorna true)
10 === "10" -> false (um e number e outro e uma string com o numero 10, e como sao de tipos diferentes, retorna false)
*/

// exemplo:

5 != "5"; "retorna false"
5 !== "5"; "retorna true"