// RECURSOS NATIVOS DO NAVEGADOR

/*
console:
.log -> mostra a variavel no console
.error -> mostra a variavel no console como erro
.warn -> mostra a variavel no console como aviso
*/

// exemplo:

let nickname = "isabelle";

console.log(nickname);
console.error(nickname);
console.warn(nickname);

/* 
windown:
.alert -> mostra a variavel como uma mensagem em um pop-up
.prompt -> mostra a variavel com uma caixa de texto (entrada de dados)
.confirm -> mostra um pop-up confirmando a acao

document
*/

// exemplo:

alert("Isso e um alerta!");
prompt("isso e uma caixa de texto, insira algo.");
confirm("isso e uma mensagem de confirmacao, voce confirma?");


